//Do-while loop:
// this is an extension of while loop.In,do-while loop the code is executed at least once.
// It performs the task first and then checks so code is execute once at least.
#include <iostream>

using namespace std;

int main()
{
int i=1; 
do{
    cout<<i<<endl;
    i++;
}
while (i<=10);// in do,while loop semi-colon after while is must.

  return 0;
}  