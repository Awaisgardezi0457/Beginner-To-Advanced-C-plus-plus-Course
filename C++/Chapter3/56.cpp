// While Loop:
// An alternate to For-loop.
//while loop is usually used when we don't know for how much time our loop will be executed.
//It is used in weird scenarios usually.
//abit easy than for-loop.in case of,multiple conditions.
#include <iostream>

using namespace std;

int main()
{
int i; // in while loop,initialization is done outside.
while (i<=10)// in while loop in parenthesis we just write condition.
{
   cout<<i<<endl;
   i++;// increment is done inside the loop. 
}
// this is while loop.
  return 0;
}  