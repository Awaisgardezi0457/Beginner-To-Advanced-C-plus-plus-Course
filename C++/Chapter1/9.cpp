// Increment and Decrement Operators:
#include <iostream>

using namespace std;

int main()
{int x;
x=4;
cout<<x<<endl;
cout<<x++<<endl;//post increment(it means first use the value of x then increment)
cout<<x<<endl;// this increases a value in 1.
x=4;
cout<<x<<endl;
cout<<++x<<endl;//pre-increment(first increment than use value of x. )
cout<<x<<endl;
  return 0;
}
